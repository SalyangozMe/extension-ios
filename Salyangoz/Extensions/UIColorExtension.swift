import UIKit
import Foundation

extension UIColor{
    public static func applicationNavigationTitleColor() -> UIColor{
        return UIColor(red: 0.04, green: 0.04, blue: 0.04, alpha: 1)
    }
    
    public static func applicationTabBarBorderTopColor() -> UIColor{
        return UIColor(red:0.70, green:0.70, blue:0.70, alpha:1.00)
    }
}